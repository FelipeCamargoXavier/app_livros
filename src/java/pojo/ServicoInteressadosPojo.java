public class ServicoInteressadosPojo {
    
    private String IDServicoInteressados;
    private String IDUsuario;
    private String IDServico;
    private String situacao;

    public String getIDServicoInteressados() {
        return IDServicoInteressados;
    }

    public void setIDServicoInteressados(String IDServicoInteressados) {
        this.IDServicoInteressados = IDServicoInteressados;
    }

    public String getIDUsuario() {
        return IDUsuario;
    }

    public void setIDUsuario(String IDUsuario) {
        this.IDUsuario = IDUsuario;
    }

    public String getIDServico() {
        return IDServico;
    }

    public void setIDServico(String IDServico) {
        this.IDServico = IDServico;
    }

    public String getSituacao() {
        return situacao;
    }

    public void setSituacao(String situacao) {
        this.situacao = situacao;
    }
    
    
}
